import BeerCard from "./BeerCard";
import type { Beer } from "./Beers";

interface BeersListProps {
  beers: Beer[];
}

function BeersList({ beers }: BeersListProps) {
  return (
    <div>
      {beers.map((beer: Beer) => (
        <BeerCard key={beer.id} beer={beer} />
      ))}
    </div>
  );
}

export default BeersList;
